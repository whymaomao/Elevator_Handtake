void RS485_Send_Data()
{
	u8 t;
	RS485_TX_EN=1;			//设置为发送模式
	
  	for(t=0;t<8;t++)		//循环发送数据
	{
	  while(USART_GetFlagStatus(USART2,USART_FLAG_TC)==RESET); //等待发送结束		
    USART_SendData(USART2,UART_send_buf[t]); //发送数据
	}	 
	while(USART_GetFlagStatus(USART2,USART_FLAG_TC)==RESET); //等待发送结束		

	delay_ms(3);
	RS485_TX_EN=0;				//设置为接收模式	
}
void voltage_send(void)
{
		RS485_TX_EN=1;			//设置为发送模式
		Send_Data = *(PointToSendBuf + Send_num);
		while(USART_GetFlagStatus(USART2,USART_FLAG_TC)!=SET);
		USART_SendData(USART2, Send_Data);
		Send_num ++;
		if(Send_num >(*PointToSendBuf))
		{
				Busy = 0;
				*PointToSendBuf = 0;
				Rev_num = 0;
				Send_num = 1;
				USART_ITConfig(USART2, USART_IT_TC, DISABLE);//关闭数据传输中断
				RS485_TX_EN=0;				//设置为接收模式	
		}
		
}

void voltage_recv(void)
{
		
		HasMeg = 1; //表示接收到了信息
		Res = USART_ReceiveData(USART2);
		if(Busy != 1)
		{
				TIM_Cmd(TIM2,DISABLE);
				TIM_SetCounter(TIM2,0);
				if((Tim_out != 0) && (Res == Mod_id))
				{
						Rcv_Complete = 0;
						Rev_num = 0;
						Rev_num ++;
						
				}	
				if((0 == Tim_out)&&(0 == Rcv_Complete))
				{
						if(Rev_num<100)
						{
								UART_recv_buf[Rev_num +1] = Res;
								Rev_num ++;
								
						}
						else
						{
								Rcv_Complete = 1;
								Busy = 1;
								UART_recv_buf[0] = Rev_num;
								Rev_num = 0;
						}
				}
		}
		Tim_out = 0;
		TIM_Cmd(TIM2,ENABLE);
}


